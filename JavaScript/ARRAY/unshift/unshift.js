/*
Método unshift
Utilize o método javascript unshift() para adicionar um ou mais elementos no início de um array e retornar o novo tamanho deste array.
*/

const modeloDeCarro = ["Fiat", "golf"]
console.log(modeloDeCarro)
modeloDeCarro.unshift("F100","Gol","Savero")
console.log(modeloDeCarro)

//[ 'F100', 'Gol', 'Savero', 'Fiat', 'golf' ]
//este metado adciona os novos elementos no inicio do meu array